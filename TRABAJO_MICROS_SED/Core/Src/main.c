void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
	if(GPIO_Pin == GPIO_PIN_0){
		if(SW == 0){
			SW = 1;
			sensor_luz = 0; //forzamos que la luz no se pueda encender por presencia
			HAL_ADC_Stop_DMA(&hadc1); // paramos la medida de la luminosidad
			HAL_TIM_IC_Stop_IT(&htim3, TIM_CHANNEL_1); // paramos la medida de la presencia
		}
		else{
			SW = 0;
			HAL_ADC_Start_DMA(&hadc1, &luz, 1); // arrancamos la medida de la luminosidad
			HAL_TIM_IC_Start_IT(&htim3, TIM_CHANNEL_1); // arrancamos la medida de la presencia
		}
	}
}


void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc){
	if(hadc->Instance == ADC1){
		// Como el DMA lo hace por nosotros, no hace falta coger el valor devuelto por el convertidor
		//como estamos usando DMA, no tenemos que tratar el valor devuelto por el convertidor
		if(luz < Umbral_light && sensor_luz == 0){
			sensor_luz = 1; // si no hay luz ambiente, podemos encender el LED
			sensor_presencia = 0;
		}
		else if(luz > Umbral_light && sensor_luz == 1){
			sensor_luz = 0; // si hay luz ambiente, no podemos encender el LED
			sensor_presencia = 0;
		}
		HAL_ADC_Start_DMA(&hadc1, &luz, 1); // arrancamos de nuevo el convertidor
	}
}




void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim){
	if(htim->Instance == TIM3){
		if (IC_1 == 0){ // si todavia no se ha producido el flanco de subida
			IC_1 = HAL_TIM_ReadCapturedValue(&htim3, TIM_CHANNEL_1);//leemos el valor del tiempo cuando ocurre el flanco de subida
			// Cambiamos la sensibilidad de la interrupción al flanco de bajada
			__HAL_TIM_SET_CAPTUREPOLARITY(&htim3, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);
		}
		else if (IC_1 != 0){ // si ya se ha producido el flanco de subida
			IC_2 = HAL_TIM_ReadCapturedValue(&htim3, TIM_CHANNEL_1);//leemos el valor del tiempo cuando ocurre el flanco de bajada

			__HAL_TIM_SET_COUNTER(htim, 0);
			if (IC_2 > IC_1){
				distancia = IC_2 - IC_1;
			}
			else if (IC_1 > IC_2){
				distancia = (0xffff - IC_1) + IC_2;
			}
			if(distancia < Umbral_distance){ // presencia detectada
				sensor_presencia = 1;
				HAL_TIM_Base_Start_IT(&htim10); // iniciamos el temporizador de 5 segundos
			}
			IC_1 = 0;
			// Cambiamos la sensibilidad de la interrupción al flanco de subida
			__HAL_TIM_SET_CAPTUREPOLARITY(&htim3, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);
		}
	}
}


void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	if(htim->Instance == TIM10){
		sensor_presencia = 0;
		HAL_TIM_Base_Stop_IT(&htim10);
	}
}
